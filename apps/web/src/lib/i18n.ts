type MessageTree = (typeof messages)[Locale];
type MessagePath =
  | `common.${keyof MessageTree['common'] & string}`
  | `auth.${keyof MessageTree['auth'] & string}`
  | `dashboard.${keyof MessageTree['dashboard'] & string}`
  | `tasks.${keyof MessageTree['tasks'] & string}`
  | `projects.${keyof MessageTree['projects'] & string}`
  | `home.${keyof MessageTree['home'] & string}`
  | `workspace.${keyof MessageTree['workspace'] & string}`;

function getMessage(locale: Locale, path: MessagePath): string {
  const [scope, key] = path.split('.') as [keyof MessageTree, string];
  return (messages[locale][scope] as Record<string, string>)[key] ?? path;
}

export function translate(
  locale: Locale,
  path: MessagePath,
  vars?: Record<string, string | number>,
): string {
  const template = getMessage(locale, path);
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, token: string) => `${vars[token] ?? `{${token}}`}`);
}

export type TranslationKey = MessagePath;
